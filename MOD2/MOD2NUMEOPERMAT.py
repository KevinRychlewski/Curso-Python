# Tipos de numeros que podem ser usados em Python
a = 20
b = 20.5
print(type(a))
print(type(b))
print(10 + 6)# soma
print(10 - 6)# subtração
print(10 * 6)# multiplicação
print(10 / 6)# divisão
print(10 // 6)# divisão de inteiro, diz quantas vezes um numero pode ser inserido dentro de outra
print(10 % 6)# modulus, mostra o restante de uma divisao
print(10 ** 6)# exponenciais, multiplica colocando tantos numeros a mais
